public class rewriteCode {
    public static void main(String[] args) {
        
        char m = 'A';
        switch (m) {
            case 'a':
                System.out.println("A");
                break;
                case 'b':
                System.out.println("B");
                break;
        
            default:
            System.out.println("invalid option");
                break;
        }
    }
}
