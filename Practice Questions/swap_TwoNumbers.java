public class swap_TwoNumbers{
	
	public static void main(String ar[]){
	int n1 = 15;
	int n2 = 30;
	int n3;
	System.out.println("VALUES BEFORE SWAPPING ");
	System.out.println("n1 = "+n1);
	System.out.println("n2 = "+n2);
	n3 = n1;
	n1 = n2;
	n2 = n3;
	System.out.println("VALUES AFTER SWAPPING ");
	System.out.println("n1 = "+n1);
	System.out.println("n2 = "+n2);
	}
	
}